class Command(BaseCommand):
    help = "Заполняет базу данных именами и фамилиями на разных языках"

    def handle(self, *args, **kwargs):
        for code, names in NAMES.items():
            language, _ = Language.objects.get_or_create(code=code)

            for first_name in names["first_names"]:
                first_name, created = NationalFirstName.objects.get_or_create(language=language, first_name=first_name)
                if created:
                    self.stdout.write(self.style.SUCCESS(f'Добавлено имя {first_name} для языка с кодом {code}'))
                else:
                    self.stdout.write(self.style.WARNING(f'Имя {first_name} уже существует для языка с кодом {code}'))

            for last_name in names["last_names"]:
                last_name, created = NationalLastName.objects.get_or_create(language=language, last_name=last_name)
                if created:
                    self.stdout.write(self.style.SUCCESS(f'Добавлена фамилия {last_name} для языка с кодом {code}'))
                else:
                    self.stdout.write(self.style.WARNING(f'Фамилия {last_name} уже существует для языка с кодом {code}'))


